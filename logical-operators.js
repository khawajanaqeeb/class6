var isMyBirthDay = true;
var amIHappy = false;
if (isMyBirthDay && amIHappy) // this is an example of logical operator and(&&)
 {
    console.log("Happy Birthday");
}
if (isMyBirthDay || amIHappy) {
    console.log("Happy Birthday but I am not happy");
} // this is an example of logical operator or(||)
